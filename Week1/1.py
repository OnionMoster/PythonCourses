def picture(m, n):
    p1 = (" " + ("/\\") * n + " ") * m
    p2 = ("/" + " " + "_" * (n * 2 - 2) + " " + "\\") * m
    p3 = ("\\" + " " + "_" * (n * 2 - 2) + " " + "/") * m
    p4 = (" " + ("\\/") * n + " ") * m
    P5 = p1 + "\n" + p2 + "\n" + p3 + "\n" + p4
    return P5


px = picture(1, 1)
print(px)
